    private void btnmodifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodifyActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
        
        if(jTable1.getSelectedRow()>=0){
            String ItemID = tblModel.getValueAt(jTable1.getSelectedRow(),0).toString();
            String ItemName = tblModel.getValueAt(jTable1.getSelectedRow(),1).toString();
            String Quantity = tblModel.getValueAt(jTable1.getSelectedRow(),3).toString();
            String NewItemID = lblItemID3.getText();
        String NewItemName = lblItemName1.getText();
        int NewPriceperitem = Integer.parseInt(TFPPI.getText());
        int NewQuantity = Integer.parseInt(TFQuantity.getText());
        int NewPrice = NewPriceperitem * NewQuantity;
        String ComID = lblComID.getText();
        String ComName = lblComName.getText();

        // Update the values in the selected row of the table
        tblModel.setValueAt(NewItemID, jTable1.getSelectedRow(), 0);
        tblModel.setValueAt(NewItemName, jTable1.getSelectedRow(), 1);
        tblModel.setValueAt(NewPriceperitem, jTable1.getSelectedRow(), 2);
        tblModel.setValueAt(NewQuantity, jTable1.getSelectedRow(), 3);
        tblModel.setValueAt(NewPrice, jTable1.getSelectedRow(), 4);
        tblModel.setValueAt(ComID, jTable1.getSelectedRow(), 5);
        tblModel.setValueAt(ComName, jTable1.getSelectedRow(), 6);

        // Create a PurchaseRequisition object with the new values and add it to the service
        PurchaseRequisition pr = new PurchaseRequisition(NewItemID, NewItemName, (int)NewPriceperitem,NewQuantity,ComID,ComName) ;
        service.deleteRequisition(ItemID, Quantity);
        service.addRequisition(pr);

        // Remove the old row from the table
        tblModel.removeRow(jTable1.getSelectedRow());
        tblModel.addRow(new Object[]{NewItemID, NewItemName, NewPriceperitem, NewQuantity, NewPrice,ComID,ComName});

        // Add the new entry to your data source (e.g., a file or database) if necessary
        String entry = NewItemID + "/" + NewItemName + "/" + NewPriceperitem + "/" + NewQuantity + "/" + NewPrice;

        // Calculate the price
        pricecalculate();

        // Show a success message
        JOptionPane.showMessageDialog(this, "Success Modify");
    } else {
        if (tblModel.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Table is Empty");
        } else {
            JOptionPane.showMessageDialog(this, "Please Select Single Row To Modify");
        }
    }

        
        lblItemID3.setText("");
        lblItemName1.setText("");
        TFPPI.setText("");
        TFQuantity.setText("");
        lblComID.setText("");
        lblComName.setText("");
    }//GEN-LAST:event_btnmodifyActionPerformed

